
#dictionary with keys and values
story1 = {
    "start": "there was a chihuahua called louis",
    "middle": "he laid all day in the sun",
    "end": "where he drank all of the rum"

}

print(story1)

# this tell us the type of data which is dictionary
print(type(story1))

# this tells us all the keys of the dictionary
print(story1.keys())

# this tells us the values of the dictionary
print(story1.values())

# here we print each value individually

print(story1["start"])
print(story1["middle"])
print(story1["end"])




